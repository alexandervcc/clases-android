package com.example.demo.dto;

public class ChefDto {
	public String nombre;
	public String apellido;
	public Integer edad;
	public String ciudad;
	public ChefDto(String nombre, String apellido, Integer edad, String ciudad) {
		super();
		this.nombre = nombre;
		this.apellido = apellido;
		this.edad = edad;
		this.ciudad = ciudad;
	}
	
	
}
